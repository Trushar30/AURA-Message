export const AppShell: React.FC = () => {
    const { user, logout } = useAuthStore();
    const navigate = useNavigate();

    const handleLogout = async () => {
        await logout();
        navigate('/login');
    };

    const navItems = [
        { path: '/app/home', icon: <HomeIcon />, label: 'Home' },
        { path: '/app/search', icon: <SearchIcon />, label: 'Search' },
        { path: '/app/request', icon: <RequestIcon />, label: 'Requests' },
        { path: '/app/teams', icon: <TeamsIcon />, label: 'Teams' },
        { path: '/app/settings', icon: <SettingsIcon />, label: 'Settings' },
        { path: '/app/profile', icon: <ProfileIcon />, label: 'Profile' },
    ];

    return (
        <div className="app-shell sidebar-collapsed">
            <aside className="app-sidebar">
                <div className="sidebar-brand">
                    <img src="/logo.svg" alt="AURA" className="brand-logo" />
                </div>

                <nav className="sidebar-nav">
                    {navItems.map((item) => (
                        <NavLink
                            key={item.path}
                            to={item.path}
                            className={({ isActive }) => `nav-item ${isActive ? 'active' : ''}`}
                            title={item.label}
                        >
                            <span className="nav-icon">{item.icon}</span>
                        </NavLink>
                    ))}
                </nav>

                <div className="sidebar-footer">
                    <div className="user-section">
                        <Avatar
                            name={user?.name || 'User'}
                            src={user?.avatar}
                            size="sm"
                            status="online"
                            showStatus
                        />
                    </div>
                    <button className="logout-btn" onClick={handleLogout} title="Logout">
                        <LogoutIcon />
                    </button>
                </div>
            </aside>

            <main className="app-main">
                <Outlet />
            </main>
        </div>
    );
};
